public class App {

    public static void main(String[] args) {
        int num = 5;
        Integer numWrapper = num;
        System.out.println("int = " + num);
        System.out.println("Integer = " + numWrapper);

        byte num2 = 10;
        Byte num2Wrapper = num2;
        System.out.println("byte = " + num2);
        System.out.println("Byte = " + num2Wrapper);
    }

}
